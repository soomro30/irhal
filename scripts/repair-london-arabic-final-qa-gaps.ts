if (!process.env.DATABASE_URL) {
  throw new Error("DATABASE_URL is required.");
}

const db = new Client({
  connectionString: process.env.DATABASE_URL,
  ssl: { rejectUnauthorized: false },
});

await db.connect();

try {
  await db.query("begin");

  const cityResult = await db.query<{
    id: number;
    translations: Record<string, unknown> | null;
  }>("select id, translations from payload.cities where slug = $1 limit 1", [
    "london",
  ]);
  const city = cityResult.rows[0];
  if (!city) throw new Error("London city row was not found.");

  const currentTranslations = city.translations ?? {};
  const currentArabic =
    typeof currentTranslations.ar === "object" && currentTranslations.ar
      ? (currentTranslations.ar as Record<string, unknown>)
      : {};

  const mergedTranslations = {
    ...currentTranslations,
    ar: {
      ...currentArabic,
      ...cityArabic,
    },
  };

  await db.query(
    `update payload.cities
     set translations = $1::jsonb,
         updated_at = now()
     where id = $2`,
    [JSON.stringify(mergedTranslations), city.id],
  );

  let updated = 0;
  for (const append of overviewAppends) {
    const result = await db.query(
      `update payload.guide_items
       set arabic_overview = concat_ws(E'\n\n', arabic_overview, $1::text),
           workflow_status = 'review'::payload.enum_guide_items_workflow_status,
           updated_at = now()
       where city_id = $2
         and kind = $3::payload.enum_guide_items_kind
         and slug = $4`,
      [append.paragraphs.join("\n\n"), city.id, append.kind, append.slug],
    );

    if (result.rowCount !== 1) {
      throw new Error(
        `Expected one London guide item for ${append.kind}:${append.slug}, got ${result.rowCount}.`,
      );
    }
    updated += 1;
  }

  await db.query(
    `insert into payload.update_logs (
       entity_type,
       entity_slug,
       change_type,
       summary,
       source_snapshot,
       verified_at,
       updated_at,
       created_at
     )
     values (
       $1,
       $2,
       'source-updated'::payload.enum_update_logs_change_type,
       $3,
       $4::jsonb,
       now(),
       now(),
       now()
     )`,
    [
      "city-arabic-qa-repair",
      "london/final-arabic-qa-gaps",
      `Closed London Arabic shell fields and expanded ${updated} guide-item overviews that were below the strict QA length threshold.`,
      JSON.stringify({
        createdBy: "scripts/repair-london-arabic-final-qa-gaps.ts",
        reason: "Close London Arabic QA before adding a new city.",
        updatedOverviewSlugs: overviewAppends.map(
          (append) => `${append.kind}:${append.slug}`,
        ),
        cityFields: Object.keys(cityArabic),
      }),
    ],
  );

  await db.query("commit");
  console.log(`Updated London city shell and ${updated} guide-item overviews.`);
} catch (error) {
  await db.query("rollback");
  throw error;
} finally {
  await db.end();
}
