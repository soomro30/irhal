if (!process.env.DATABASE_URL) {
  throw new Error("DATABASE_URL is required.");
}

const db = new Client({
  connectionString: process.env.DATABASE_URL,
  ssl: { rejectUnauthorized: false },
});

const sourceSnapshot = {
  createdBy: "scripts/repair-london-arabic-guide-item-gaps.ts",
  reason:
    "Close London Arabic guide-item field gaps before adding the next city.",
  sourceBasis:
    "Existing Payload English guide-item summaries/bodies plus stored editorial source rows.",
};

await db.connect();

try {
  await db.query("begin");

  const cityResult = await db.query<{ id: number }>(
    "select id from payload.cities where slug = $1 limit 1",
    ["london"],
  );
  const cityId = cityResult.rows[0]?.id;
  if (!cityId) throw new Error("London city row was not found.");

  let updated = 0;
  for (const repair of repairs) {
    const result = await db.query<{ id: number }>(
      `update payload.guide_items
       set
         arabic_title = $1,
         arabic_summary = $2,
         arabic_overview = $3,
         arabic_area = $4,
         arabic_category = $5,
         arabic_address = coalesce($6, arabic_address),
         workflow_status = 'review'::payload.enum_guide_items_workflow_status,
         updated_at = now()
       where city_id = $7
         and kind = $8::payload.enum_guide_items_kind
         and slug = $9
       returning id`,
      [
        repair.title,
        repair.summary,
        repair.overview.join("\n\n"),
        repair.area,
        repair.category,
        repair.address ?? null,
        cityId,
        repair.kind,
        repair.slug,
      ],
    );

    if (result.rowCount !== 1) {
      throw new Error(
        `Expected one London guide item for ${repair.kind}:${repair.slug}, got ${result.rowCount}.`,
      );
    }
    updated += 1;
  }

  await db.query(
    `insert into payload.update_logs (
       entity_type,
       entity_slug,
       change_type,
       summary,
       source_snapshot,
       verified_at,
       updated_at,
       created_at
     )
     values (
       $1,
       $2,
       'source-updated'::payload.enum_update_logs_change_type,
       $3,
       $4::jsonb,
       now(),
       now(),
       now()
     )`,
    [
      "guide-item-bulk-repair",
      "london/arabic-guide-item-gaps",
      `Added Gulf-editorial Arabic fields to ${updated} London guide items and expanded Westminster Abbey to satisfy the strict Arabic QA gate.`,
      JSON.stringify({
        ...sourceSnapshot,
        updated,
        slugs: repairs.map((repair) => `${repair.kind}:${repair.slug}`),
      }),
    ],
  );

  await db.query("commit");
  console.log(`Updated ${updated} London guide items.`);
} catch (error) {
  await db.query("rollback");
  throw error;
} finally {
  await db.end();
}
